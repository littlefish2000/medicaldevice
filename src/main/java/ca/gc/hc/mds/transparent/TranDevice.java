package ca.gc.hc.mds.transparent;

public class TranDevice {
	


    private Long deviceId;


    private String tradeName;


    private String perfnameCode;	    

	private Long riskClassification;
	

	private String usageCode;	
	
	

	private Long superSededID;		
	

	private Long superSedesID;


	public Long getDeviceId() {
		return deviceId;
	}


	public void setDeviceId(Long deviceId) {
		this.deviceId = deviceId;
	}


	public String getTradeName() {
		return tradeName;
	}


	public void setTradeName(String tradeName) {
		this.tradeName = tradeName;
	}


	public String getPerfnameCode() {
		return perfnameCode;
	}


	public void setPerfnameCode(String perfnameCode) {
		this.perfnameCode = perfnameCode;
	}


	public Long getRiskClassification() {
		return riskClassification;
	}


	public void setRiskClassification(Long riskClassification) {
		this.riskClassification = riskClassification;
	}


	public String getUsageCode() {
		return usageCode;
	}


	public void setUsageCode(String usageCode) {
		this.usageCode = usageCode;
	}


	public Long getSuperSededID() {
		return superSededID;
	}


	public void setSuperSededID(Long superSededID) {
		this.superSededID = superSededID;
	}


	public Long getSuperSedesID() {
		return superSedesID;
	}


	public void setSuperSedesID(Long superSedesID) {
		this.superSedesID = superSedesID;
	}		


}
